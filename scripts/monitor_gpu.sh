#!/bin/bash
################################################################################
# GPU 실시간 모니터링 스크립트
#
# 사용법:
#   bash monitor_gpu.sh          # 1초마다 갱신
#   bash monitor_gpu.sh 5        # 5초마다 갱신
################################################################################

INTERVAL=${1:-1}

echo "GPU 실시간 모니터링 (Ctrl+C로 종료)"
echo ""

watch -n $INTERVAL nvidia-smi
